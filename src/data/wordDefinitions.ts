export function getWordDefinition(word: string): string {
  if (!word) return "";
  // Strip parentheses and comments, e.g. "ঊর্ণ (মাকড়সার জাল)" -> "ঊর্ণ"
  const cleanWord = word.replace(/\(.*?\)/g, "").trim();
  return BENGALI_WORD_DEFINITIONS[cleanWord] || BENGALI_WORD_DEFINITIONS[word] || `এটি "${word}" দিয়ে তৈরি একটি চমৎকার বাংলা শব্দ!`;
}
